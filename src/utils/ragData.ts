// Filter chapters by course and level
export function getFilteredChapters(course?: string, level?: string): ChapterWithSubject[] {
  const allChapters = getAllChapters();
  
  return allChapters.filter(chapter => {
    if (course && chapter.course !== course) return false;
    if (level && chapter.level !== level) return false;
    return true;
  });
}
