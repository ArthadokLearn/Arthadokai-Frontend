interface ChapterWithSubject {
  id: string;
  title: string;
  description: string;
  keyPoints: string[];
  example: string;
  difficulty: 'beginner' | 'intermediate' | 'advanced';
  formula?: string;
  subjectName: string;
  course: string;
  level: string;
}

